package ScratchLearning;
import java.util.*;
public class InputStatements {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        String name=sc.nextLine();
        /*
        String-nextLine(); [for printing entire line] ; next(); [for printing just the first token]
        Int-nextInt();
        Float=nextFloat();
         */
        System.out.println(name);
    }
}
